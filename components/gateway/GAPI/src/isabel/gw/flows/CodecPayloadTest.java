package isabel.gw.flows;

import org.junit.After;
import org.junit.AfterClass;
import org.junit.Before;
import org.junit.BeforeClass;
import org.junit.Test;

import static org.junit.Assert.*;


public class CodecPayloadTest {

	@BeforeClass
	public static void setUpBeforeClass() throws Exception {
	}

	@AfterClass
	public static void tearDownAfterClass() throws Exception {
	}

	@Before
	public void setUp() throws Exception {
	}

	@After
	public void tearDown() throws Exception {
	}

	/**
	 * Test that I can find a existing codec.
	 */
	@Test
	public void getExistingCodec() {

		int pt = CodecPayload.getPayloadType("MPEG4");

		assertTrue(pt == 100);
	}

	/**
	 * Test that I can not find a non existing codec.
	 */
	@Test
	public void getNoExistingCodec() {

		int pt = CodecPayload.getPayloadType("NO_EXISTING_CODEC");

		assertTrue(pt == -1);
	}
}
